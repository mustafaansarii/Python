import math

# Basic Arithmetic
print(math.sqrt(25))    # Square root: 5.0
print(math.pow(2, 3))    # Power: 8.0
print(math.exp(2))       # Exponential function: e^2 â‰ˆ 7.389

# Trigonometric Functions
print(math.sin(math.radians(30)))   # Sine of 30 degrees
print(math.cos(math.radians(60)))   # Cosine of 60 degrees
print(math.tan(math.radians(45)))   # Tangent of 45 degrees

# Logarithmic Functions
print(math.log(16, 2))    # Log base 2 of 16: 4.0
print(math.log10(100))    # Log base 10 of 100: 2.0

# Angular Conversion
print(math.degrees(math.pi))    # Convert radians to degrees: 180.0
print(math.radians(90))          # Convert degrees to radians: 1.5708

# Constants
print(math.pi)          # Pi: 3.141592653589793
print(math.e)           # Euler's number: 2.718281828459045

# Ceiling and Floor
print(math.ceil(4.3))    # Ceiling: 5
print(math.floor(4.9))   # Floor: 4
